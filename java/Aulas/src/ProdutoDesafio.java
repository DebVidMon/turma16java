
public class ProdutoDesafio {
	public int codigo;
	public String descricao;
	public double valorUnitario;
	
	public ProdutoDesafio(int codigo) {
		this.codigo = codigo;
	}
	
	public ProdutoDesafio(int codigo, String descricao) {
		this.codigo = codigo;
		this.descricao = descricao;
	}
	
	public ProdutoDesafio(int codigo, String descricao, double valorUnitario) {
		this.codigo = codigo;
		this.descricao = descricao;
		this.valorUnitario = valorUnitario;
	}
	
	
}
